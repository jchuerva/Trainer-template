def update_template_section(template_path, section_content, start_marker, end_marker):
    """Update a specific section in a template file marked by HTML comments."""
    if not template_path.exists():
        print(f"Warning: Template not found: {template_path}", file=sys.stderr)
        return False
    
    try:
        # Read current template
        with open(template_path, 'r', encoding='utf-8') as f:
            content = f.read()
    except (IOError, OSError) as e:
        print(f"Error reading template {template_path}: {e}", file=sys.stderr)
        return False
    
    # Check if section already exists
    if start_marker in content:
        # Remove old section
        start_idx = content.find(start_marker)
        end_idx = content.find(end_marker)
        if end_idx != -1:
            # Find the end of the line after the end marker
            end_line = content.find('\n', end_idx)
            if end_line != -1:
                end_idx = end_line + 1
            else:
                end_idx = end_idx + len(end_marker)
            
            # Remove the section
            content = content[:start_idx] + content[end_idx:]
    
    # For PERSONAL INFORMATION section, insert after title
    # For TRAINING PREFERENCES section, insert before "Training roadmap" or at appropriate location
    lines = content.split('\n')
    insert_idx = 0
    
    if "PERSONAL INFORMATION" in start_marker:
        # Insert after first heading
        for i, line in enumerate(lines):
            if line.startswith('#'):
                insert_idx = i + 1
                break
    elif "TRAINING PREFERENCES" in start_marker:
        # Find the "## Training constraints / preferences" line or similar
        for i, line in enumerate(lines):
            if "## Training constraints / preferences" in line or \
               "## Training roadmap" in line or \
               (line.startswith("## ") and "Training" in line and ("constraint" in line.lower() or "preference" in line.lower())):
                insert_idx = i
                # Remove the existing line if it's the section header
                if "## Training constraints / preferences" in line:
                    lines.pop(i)
                    # Also remove following lines until we hit another section or empty lines
                    while i < len(lines) and lines[i].strip() and not lines[i].startswith('#'):
                        lines.pop(i)
                break
        
        if insert_idx == 0:
            # If not found, insert before "Training roadmap"
            for i, line in enumerate(lines):
                if "## Training roadmap" in line:
                    insert_idx = i
                    break
    elif "TRAINING STATUS" in start_marker:
        # Insert after PERSONAL INFORMATION section (after the --- separator)
        # or after the first heading if no personal info section
        found_personal_info_end = False
        for i, line in enumerate(lines):
            if "<!-- END PERSONAL INFORMATION -->" in line:
                insert_idx = i + 1
                found_personal_info_end = True
                break
        
        if not found_personal_info_end:
            # Insert after first heading
            for i, line in enumerate(lines):
                if line.startswith('#'):
                    insert_idx = i + 1
                    break
    
    # Clean up extra blank lines before insertion point
    while insert_idx > 0 and insert_idx < len(lines) and lines[insert_idx - 1].strip() == '':
        lines.pop(insert_idx - 1)
        insert_idx -= 1
    
    # Insert section
    lines.insert(insert_idx, '')
    lines.insert(insert_idx + 1, section_content.rstrip())
    
    # Write updated content
    updated_content = '\n'.join(lines)
    try:
        with open(template_path, 'w', encoding='utf-8') as f:
            f.write(updated_content)
    except (IOError, OSError) as e:
        print(f"Error writing template {template_path}: {e}", file=sys.stderr)
        return False
    
    return True


def update_template(template_path, personal_info):
    """Update a template file with personal information section."""
    return update_template_section(
        template_path, 
        personal_info, 
        "<!-- PERSONAL INFORMATION -->", 
        "<!-- END PERSONAL INFORMATION -->"
    )


def remove_template_section(template_path, start_marker, end_marker):
    """Remove a section from a template file if it exists."""
    if not template_path.exists():
        return True  # Nothing to remove
    
    try:
        with open(template_path, 'r', encoding='utf-8') as f:
            content = f.read()
    except (IOError, OSError):
        return False
    
    if start_marker not in content:
        return True  # Section doesn't exist, nothing to remove
    
    # Find and remove the section
    start_idx = content.find(start_marker)
    end_idx = content.find(end_marker)
    if end_idx != -1:
        # Find the end of the line after the end marker
        end_line = content.find('\n', end_idx)
        if end_line != -1:
            end_idx = end_line + 1
        else:
            end_idx = end_idx + len(end_marker)
        
        # Remove the section and any extra blank lines
        content = content[:start_idx] + content[end_idx:]
        # Clean up multiple blank lines
        while '\n\n\n' in content:
            content = content.replace('\n\n\n', '\n\n')
    
    try:
        with open(template_path, 'w', encoding='utf-8') as f:
            f.write(content)
    except (IOError, OSError):
        return False
    
    return True


def main():
    """Main entry point."""
    print("🏃 Setting up personal training information...")
    print()
    
    # Read configuration
    config = read_config()
    if config is None:
        return 1
    
    print("✅ Configuration loaded successfully")
    
    # Update templates with appropriate personal info
    repo_root = get_repo_root()
    templates_dir = repo_root / "templates"
    
    templates = {
        "goal-template.md": format_personal_info_for_goal,
        "week-plan-template.md": format_personal_info_for_week_plan,
        "workout-analysis-template.md": format_personal_info_for_workout_analysis
    }
    
    updated_count = 0
    for template_name, formatter_func in templates.items():
        template_path = templates_dir / template_name
        print(f"Updating {template_name}...", end=' ')
        
        # Update personal information section
        personal_info = formatter_func(config)
        success = update_template(template_path, personal_info)
        
        # For goal template, also update training preferences section
        if template_name == "goal-template.md" and success:
            training_prefs = format_training_preferences_for_goal(config)
            success = update_template_section(
                template_path,
                training_prefs,
                "<!-- TRAINING PREFERENCES -->",
                "<!-- END TRAINING PREFERENCES -->"
            )
        
        # For week plan template, also update training status section
        if template_name == "week-plan-template.md" and success:
            training_status = format_training_status(config)
            if training_status:  # Only add if status is not 'active' with no note
                success = update_template_section(
                    template_path,
                    training_status,
                    "<!-- TRAINING STATUS -->",
                    "<!-- END TRAINING STATUS -->"
                )
            else:
                # Remove training status section if it exists and status is active
                remove_template_section(
                    template_path,
                    "<!-- TRAINING STATUS -->",
                    "<!-- END TRAINING STATUS -->"
                )
        
        if success:
            print("✅")
            updated_count += 1
        else:
            print("❌")
    
    print()
    print(f"✅ Updated {updated_count}/{len(templates)} templates")
    print()
    print("📝 Personal information has been added to all templates.")
    print("   AI agents will now use this information for better training plans!")
    print()
    print("💡 To update the information:")
    print("   1. Edit config/config.yaml with your latest data")
    print("   2. Run: python3 scripts/setup.py")
    
    return 0


if __name__ == "__main__":
    sys.exit(main())
