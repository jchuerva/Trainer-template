#!/usr/bin/env python3
"""
Setup script to inject personal information into training templates.

This script reads personal information from config/config.yaml and adds it to the
beginning of each template file. This helps AI agents generate more suitable
and realistic training plans, workouts, and analyses.

Usage:
    python3 scripts/setup.py
"""

import sys
from pathlib import Path

sys.path.insert(0, str(Path(__file__).parent))
from utils import get_config_path, get_repo_root

import yaml


def read_config():
    """Read configuration from config/config.yaml."""
    config_path = get_config_path()
    
    if not config_path.exists():
        print(f"Error: config/config.yaml not found at {config_path}", file=sys.stderr)
        print("Please create config/config.yaml with your personal training information.", file=sys.stderr)
        return None
    
    try:
        with open(config_path, 'r', encoding='utf-8') as f:
            config = yaml.safe_load(f)
        return config
    except yaml.YAMLError as e:
        print(f"Error parsing config/config.yaml: {e}", file=sys.stderr)
        return None
    except (IOError, OSError) as e:
        print(f"Error reading config/config.yaml: {e}", file=sys.stderr)
        return None


def format_personal_info_for_goal(config):
    """Format personal information for goal template."""
    lines = []
    
    lines.append("<!-- PERSONAL INFORMATION -->")
    lines.append("<!-- This section is auto-generated by scripts/setup.py from config/config.yaml -->")
    lines.append("<!-- DO NOT EDIT THIS SECTION MANUALLY - Edit config/config.yaml instead -->")
    lines.append("")
    lines.append("## 👤 Personal Information")
    lines.append("")
    
    runner = config.get('athlete') or config.get('runner', {})
    
    # Date of birth only - training preferences go in "Training constraints / preferences" section
    if runner.get('date_of_birth'):
        lines.append(f"- **Date of birth:** {runner['date_of_birth']}")
    
    lines.append("")
    lines.append("---")
    lines.append("")
    lines.append("<!-- END PERSONAL INFORMATION -->")
    lines.append("")
    
    return "\n".join(lines)


def format_training_preferences_for_goal(config):
    """Format training preferences section for goal template."""
    lines = []
    
    lines.append("<!-- TRAINING PREFERENCES -->")
    lines.append("<!-- This section is auto-generated by scripts/setup.py from config/config.yaml -->")
    lines.append("<!-- DO NOT EDIT THIS SECTION MANUALLY - Edit config/config.yaml instead -->")
    lines.append("")
    lines.append("## Training constraints / preferences")
    
    prefs = config.get('preferences', {})
    
    # Run days
    run_days = prefs.get('run_days', [])
    if run_days:
        days_str = " / ".join(run_days)
        lines.append(f"- Run days: **{days_str}**")
    else:
        lines.append("- Run days: **[Days]**")
    
    # Strength training day
    strength_day = prefs.get('strength_day', '')
    if strength_day:
        lines.append(f"- Strength: typically **{strength_day}** (not tracked in repo)")
    else:
        lines.append("- Strength: typically **[Day]** (not tracked in repo)")
    
    lines.append("- Additional notes: ...")
    lines.append("")
    lines.append("<!-- END TRAINING PREFERENCES -->")
    lines.append("")
    
    return "\n".join(lines)


def format_training_status(config):
    """Format training status for templates."""
    status_config = config.get('training_status', {})
    status = status_config.get('status', 'active')
    
    # If active with no note, return empty (no need to add section)
    if status == 'active' and not status_config.get('note'):
        return None
    
    lines = []
    lines.append("<!-- TRAINING STATUS -->")
    lines.append("<!-- This section is auto-generated by scripts/setup.py from config/config.yaml -->")
    lines.append("<!-- DO NOT EDIT THIS SECTION MANUALLY - Edit config/config.yaml instead -->")
    lines.append("")
    
    # Status emoji mapping
    status_emoji = {
        'active': '🟢',
        'sick': '🤒',
        'injury': '🩹',
        'holidays': '🏖️',
        'returning': '🔄'
    }
    
    emoji = status_emoji.get(status, '⚪')
    lines.append(f"## {emoji} Training Status: {status.title()}")
    lines.append("")
    
    # Add note if present
    if status_config.get('note'):
        lines.append(f"**Note:** {status_config['note']}")
        lines.append("")
    
    # Add AI guidance based on status
    guidance = {
        'sick': "<!-- **AI Coach Guidance:** Runner is currently sick. Prioritize rest and recovery. If training is planned, significantly reduce intensity and volume. Consider skipping workouts entirely if symptoms are severe. -->",
        'injury': "<!-- **AI Coach Guidance:** Runner is currently injured. Avoid exercises that stress the injured area. Focus on recovery and alternative low-impact activities if approved. -->",
        'holidays': "<!-- **AI Coach Guidance:** Runner is on holidays. Plan lighter, flexible workouts that can be done without usual equipment/routes. Maintain base fitness without strict targets. -->",
        'returning': "<!-- **AI Coach Guidance:** Runner is returning from a break. Apply gradual ramp-up protocol: start at 50-60% of previous volume, increase slowly over 2-3 weeks. Avoid intensity work in the first week. -->"
    }
    
    if status in guidance:
        lines.append(guidance[status])
        lines.append("")
    
    lines.append("---")
    lines.append("")
    lines.append("<!-- END TRAINING STATUS -->")
    lines.append("")
    
    return "\n".join(lines)


